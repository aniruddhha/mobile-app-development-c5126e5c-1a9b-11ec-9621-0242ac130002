public class Variables {
    public static void main(String[] args) {
        int age = 20; // variable
        age = 30;
        boolean isOs = true;
        double price = 56.5;
        char ch = 's';

        System.out.println("Age is " + age);
        System.out.println("Is OS " + isOs);
        System.out.println("Price " + price);
        System.out.println("Char is " + ch);
    }
}
